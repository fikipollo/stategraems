package classes;

/**
 *
 * @author rhernandez
 */
public class ExtraField {
    String name;
    String value;
    String label;
    String section;

    public ExtraField() {
    }

    public ExtraField(String name, String value, String label, String section) {
        this.name = name;
        this.value = value;
        this.label = label;
        this.section = section;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getSection() {
        return section;
    }

    public void setSection(String section) {
        this.section = section;
    }

}
